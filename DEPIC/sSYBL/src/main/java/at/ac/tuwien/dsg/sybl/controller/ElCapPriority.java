package at.ac.tuwien.dsg.sybl.controller;

/**
 *
 * @author Jun
 */
public class ElCapPriority {
    
    int index;
    int priority;

    public ElCapPriority(int index, int priority) {
        this.index = index;
        this.priority = priority;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getPriority() {
        return priority;
    }

    public void setPriority(int priority) {
        this.priority = priority;
    }

    

   

    
    
  
}
