package at.ac.tuwien.dsg.edasich.entity.daf.datasource;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Jun
 */
@XmlRootElement(name = "dataSourceMySQL")
@XmlAccessorType(XmlAccessType.FIELD)
public class DataSourceMySQL {
    
    @XmlElement(name = "ip", required = true)
    String ip;
    
    @XmlElement(name = "port", required = true)
    String port;
    
    @XmlElement(name = "database", required = true)
    String database;
    
    @XmlElement(name = "username", required = true)
    String username;
    
    @XmlElement(name = "password", required = true)
    String password;

    public DataSourceMySQL() {
    }

    public DataSourceMySQL(String ip, String port, String database, String username, String password) {
        this.ip = ip;
        this.port = port;
        this.database = database;
        this.username = username;
        this.password = password;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getDatabase() {
        return database;
    }

    public void setDatabase(String database) {
        this.database = database;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
    
    
}
